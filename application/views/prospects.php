           if (x) {
               $.ajax({
                   data: { 'id' : id,'table':table},
                   type: "post",
                   url: url + "index.php/Application/deleteRow",
                   success: function(data){
                       $('#current_row'+id).remove();
                   }
               });
               return true;
           } else {
               return false;
           }


       }


